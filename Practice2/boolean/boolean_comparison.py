#Comparing two numbers
print(10 > 5) #true
print(9 < 10) #true

#Check equality between numbers
print(9 == 7) #false

#Not equal
print(6 != 7) #true

#Boolean comparison in variable
nine = 9
is_nine = nine == 10
print(is_nine) #false

#Comparing strings
print("USA" == "China") #false

#Greater than or Equal
print(10 >= 50) #false

#Less than or Equal
print(60 <= 80) #true